package day19_loops;

public class MutliplicationTable {
    public static void main(String[] args) {

        int num = 5;

        for (int i = 0; i <= 10; i++) {
            System.out.println(num + " x " + i + " = " + num * i);
        }

        for (int i = 0; i <= 9; i++) {
            System.out.println(num + " x " + i + " = " + num * i);
        }




    }
}
